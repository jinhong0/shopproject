package test01;

// 초기 파일
public class Calculater {
	
	public int sum(int num1, int num2) {
		return num1 + num2;
	}
	
	public int substraction(int num1, int num2) {
		return num1 - num2;
	}
	
	public int multiplication(int num1, int num2) {
		return num1 * num2;
	}
	
	public int division(int num1, int num2) {
		return num1 / num2;
	}

}
